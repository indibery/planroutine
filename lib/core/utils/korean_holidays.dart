/// 대한민국 공휴일 판정 (내장 테이블 — 오프라인·의존성 0).
///
/// 고정 공휴일(신정·삼일절·어린이날·현충일·광복절·개천절·한글날·성탄절)은 규칙으로,
/// 음력 기반(설·추석 연휴, 부처님오신날)과 대체공휴일·선거일은 연도별 테이블로 판정.
/// 테이블 범위 밖 연도는 고정 공휴일만 판정된다(음력·대체는 미상).
///
/// ※ 연도 추가 시: 아래 `_lunarAndSubstitute`에 해당 연도 항목을 한 줄씩 추가.
///   (설·추석 연휴 3일, 부처님오신날, 대체공휴일, 선거일)
library;

/// 고정 공휴일 (월, 일)
const _fixed = {
  (1, 1), // 신정
  (3, 1), // 삼일절
  (5, 5), // 어린이날
  (6, 6), // 현충일
  (8, 15), // 광복절
  (10, 3), // 개천절
  (10, 9), // 한글날
  (12, 25), // 성탄절
};

/// 연도별 음력 공휴일 + 대체공휴일 + 선거·임시공휴일 (월, 일)
const _lunarAndSubstitute = <int, Set<(int, int)>>{
  2025: {
    (1, 27), // 임시공휴일
    (1, 28), (1, 29), (1, 30), // 설날 연휴
    (3, 3), // 삼일절 대체
    (5, 6), // 어린이날·부처님오신날(5/5 겹침) 대체
    (6, 3), // 대통령선거
    (10, 5), (10, 6), (10, 7), // 추석 연휴
    (10, 8), // 추석 대체
  },
  2026: {
    (2, 16), (2, 17), (2, 18), // 설날 연휴
    (3, 2), // 삼일절(일) 대체
    (5, 24), // 부처님오신날
    (5, 25), // 부처님오신날(일) 대체
    (6, 3), // 지방선거
    (8, 17), // 광복절(토) 대체
    (9, 24), (9, 25), (9, 26), // 추석 연휴
    (10, 5), // 개천절(토) 대체
  },
  2027: {
    (2, 6), (2, 7), (2, 8), // 설날 연휴
    (2, 9), // 설(일 포함) 대체
    (5, 13), // 부처님오신날
    (8, 16), // 광복절(일) 대체
    (9, 14), (9, 15), (9, 16), // 추석 연휴
    (10, 4), // 개천절(일) 대체
    (10, 11), // 한글날(토) 대체
    (12, 27), // 성탄절(토) 대체
  },
};

/// [date]가 대한민국 공휴일이면 true.
bool isKoreanHoliday(DateTime date) {
  final md = (date.month, date.day);
  if (_fixed.contains(md)) return true;
  return _lunarAndSubstitute[date.year]?.contains(md) ?? false;
}
